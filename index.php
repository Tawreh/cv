    <section class="what">
      <h2>What I can bring to the table</h2>
      <p>I’ve been working with the web since I was 14, and since then I’ve become a huge believer in making things simple and easy to use, both for the end user and for any developer who will take up work after me. I’m keen to broaden my horizons and learn new things, rather than stagnating in a single place.</p>
      <p>During my placement and subsequent employment I feel I’ve become a stronger team player, and I work well with like-minded and enthusiastic people. However, I thrive best when given a task to do and a deadline, and am capable of putting my head down and concentrating on a single project.</p>
      <p>I enjoy learning and sharing new knowledge with others. I keep up with news on Twitter, listen to web-related podcasts, and, when possible, I attend conferences. As a placement student I learned from my colleagues, and try to pass on that help to students in turn, whenever possible. Occasionally I <a href="http://herbal-jazz.net/category/work/drupal/">blog about the work I do</a>, and these posts have proven to be some of the most popular I’ve written.</p>
      <p>Oh ... and I really like writing documentation! I know that sounds weird, but I think it’s important to make it as easy as possible for others to pick up systems. I get frustrated when documentation isn’t provided, so I do my best to provide it for others. I have some of my documentation samples <a href="https://drive.google.com/folderview?id=0B8GSoyQxjjZ1TTRpMF91UlNtUDg&usp=sharing">saved on Google Drive</a>.</p>
